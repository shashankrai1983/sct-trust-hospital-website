#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
require('dotenv').config();

console.log('üîç SCT Trust Hospital - Production Readiness Assessment');
console.log('=' .repeat(60));

const issues = [];
const warnings = [];
const recommendations = [];

// 1. Environment Variables Check
console.log('\n1. üåç Environment Variables');
console.log('-'.repeat(30));

const requiredEnvVars = [
  'MONGODB_URI',
  'MONGODB_DB', 
  'NEXTAUTH_SECRET',
  'ADMIN_EMAIL',
  'ADMIN_PASSWORD_HASH',
  'RECAPTCHA_SECRET_KEY',
  'NEXT_PUBLIC_RECAPTCHA_SITE_KEY'
];

const envFile = path.join(process.cwd(), '.env');
let envExists = false;
let envContent = '';

try {
  envContent = fs.readFileSync(envFile, 'utf8');
  envExists = true;
  console.log('‚úÖ .env file exists and is readable');
} catch (error) {
  issues.push('‚ùå .env file missing or unreadable');
}

requiredEnvVars.forEach(varName => {
  const value = process.env[varName];
  if (!value) {
    issues.push(`‚ùå Missing environment variable: ${varName}`);
  } else if (varName === 'NEXTAUTH_SECRET' && value.length < 32) {
    warnings.push(`‚ö†Ô∏è NEXTAUTH_SECRET should be at least 32 characters (current: ${value.length})`);
  } else if (varName === 'ADMIN_PASSWORD_HASH' && !value.startsWith('$2b$')) {
    issues.push(`‚ùå ADMIN_PASSWORD_HASH is not a valid bcrypt hash`);
  } else {
    console.log(`‚úÖ ${varName}: ${varName.includes('SECRET') || varName.includes('HASH') ? '[REDACTED]' : value}`);
  }
});

// 2. Security Configuration
console.log('\n2. üõ°Ô∏è Security Configuration');
console.log('-'.repeat(30));

// Check HTTPS requirements for production
console.log('üìã HTTPS/SSL Configuration:');
if (process.env.NODE_ENV === 'production') {
  if (!process.env.NEXTAUTH_URL || !process.env.NEXTAUTH_URL.startsWith('https://')) {
    issues.push('‚ùå NEXTAUTH_URL must use HTTPS in production');
  } else {
    console.log('‚úÖ NEXTAUTH_URL configured for HTTPS');
  }
} else {
  recommendations.push('üí° Set NEXTAUTH_URL to HTTPS domain for production deployment');
}

// Check password strength
const adminEmail = process.env.ADMIN_EMAIL;
if (adminEmail && adminEmail.includes('admin@')) {
  warnings.push('‚ö†Ô∏è Consider using a non-obvious admin email for production');
}

// Check session security
console.log('üîê Session Security:');
console.log('‚úÖ JWT strategy enabled with 8-hour expiration');
console.log('‚úÖ bcrypt password hashing implemented');
console.log('‚úÖ Environment variable fallback mechanism in place');

// 3. Database Configuration
console.log('\n3. üóÑÔ∏è Database Configuration');
console.log('-'.repeat(30));

const mongoUri = process.env.MONGODB_URI;
if (mongoUri) {
  if (mongoUri.includes('mongodb+srv://')) {
    console.log('‚úÖ Using MongoDB Atlas (recommended for production)');
  } else if (mongoUri.includes('localhost') || mongoUri.includes('127.0.0.1')) {
    warnings.push('‚ö†Ô∏è Using local MongoDB - not suitable for production');
  }
  
  if (!mongoUri.includes('retryWrites=true')) {
    recommendations.push('üí° Add retryWrites=true to MongoDB URI for better reliability');
  }
  
  if (!mongoUri.includes('w=majority')) {
    recommendations.push('üí° Add w=majority to MongoDB URI for better durability');
  }
} else {
  issues.push('‚ùå MongoDB URI not configured');
}

// 4. Application Configuration
console.log('\n4. ‚öôÔ∏è Application Configuration');
console.log('-'.repeat(30));

// Check next.config.js
const nextConfigPath = path.join(process.cwd(), 'next.config.js');
try {
  const nextConfigContent = fs.readFileSync(nextConfigPath, 'utf8');
  
  if (nextConfigContent.includes('trailingSlash: false')) {
    console.log('‚úÖ Trailing slash disabled (good for NextAuth)');
  }
  
  if (nextConfigContent.includes('output:')) {
    console.log('‚úÖ Output configuration found');
  } else {
    recommendations.push('üí° Consider adding output configuration for deployment');
  }
  
} catch (error) {
  warnings.push('‚ö†Ô∏è next.config.js not found or unreadable');
}

// Check package.json for production scripts
const packageJsonPath = path.join(process.cwd(), 'package.json');
try {
  const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, 'utf8'));
  
  if (packageJson.scripts?.build) {
    console.log('‚úÖ Build script available');
  } else {
    issues.push('‚ùå No build script found in package.json');
  }
  
  if (packageJson.scripts?.start) {
    console.log('‚úÖ Production start script available');
  } else {
    issues.push('‚ùå No start script found in package.json');
  }
  
  // Check for production dependencies
  const prodDeps = Object.keys(packageJson.dependencies || {});
  const devDeps = Object.keys(packageJson.devDependencies || {});
  
  console.log(`üì¶ Dependencies: ${prodDeps.length} production, ${devDeps.length} development`);
  
} catch (error) {
  issues.push('‚ùå package.json not found or invalid');
}

// 5. API Security
console.log('\n5. üîí API Security');
console.log('-'.repeat(30));

// Check API route protection
const pagesApiPath = path.join(process.cwd(), 'pages', 'api');
if (fs.existsSync(pagesApiPath)) {
  const dashboardStatsPath = path.join(pagesApiPath, 'dashboard', 'stats.ts');
  const dashboardAppointmentsPath = path.join(pagesApiPath, 'dashboard', 'appointments.ts');
  
  [dashboardStatsPath, dashboardAppointmentsPath].forEach(filePath => {
    if (fs.existsSync(filePath)) {
      const content = fs.readFileSync(filePath, 'utf8');
      if (content.includes('getServerSession')) {
        console.log(`‚úÖ ${path.basename(filePath)} has authentication protection`);
      } else {
        issues.push(`‚ùå ${path.basename(filePath)} lacks authentication protection`);
      }
    }
  });
}

// Check reCAPTCHA configuration
if (process.env.RECAPTCHA_SECRET_KEY && process.env.NEXT_PUBLIC_RECAPTCHA_SITE_KEY) {
  console.log('‚úÖ reCAPTCHA configured for form protection');
} else {
  warnings.push('‚ö†Ô∏è reCAPTCHA not fully configured - forms vulnerable to spam');
}

// 6. Error Handling & Logging
console.log('\n6. üìù Error Handling & Logging');
console.log('-'.repeat(30));

// Check API error handling
const appointmentsApiPath = path.join(process.cwd(), 'app', 'api', 'appointments', 'route.ts');
if (fs.existsSync(appointmentsApiPath)) {
  const content = fs.readFileSync(appointmentsApiPath, 'utf8');
  if (content.includes('try {') && content.includes('catch')) {
    console.log('‚úÖ Appointments API has error handling');
  }
  if (content.includes('console.error')) {
    console.log('‚úÖ Error logging implemented');
  }
}

// 7. Performance & Optimization
console.log('\n7. ‚ö° Performance & Optimization');
console.log('-'.repeat(30));

// Check for optimization features
const buildOutputPath = path.join(process.cwd(), '.next');
if (fs.existsSync(buildOutputPath)) {
  console.log('‚úÖ Next.js build output exists');
  recommendations.push('üí° Run production build and analyze bundle size');
} else {
  recommendations.push('üí° Run "npm run build" to check for build issues');
}

// Check image optimization
const publicPath = path.join(process.cwd(), 'public');
if (fs.existsSync(publicPath)) {
  const images = fs.readdirSync(publicPath).filter(f => 
    f.match(/\.(jpg|jpeg|png|gif|webp)$/i)
  );
  if (images.length > 0) {
    console.log(`üì∏ Found ${images.length} images in public folder`);
    recommendations.push('üí° Consider optimizing images for production (WebP format, compression)');
  }
}

// 8. Deployment Readiness
console.log('\n8. üöÄ Deployment Readiness');
console.log('-'.repeat(30));

// Check for common deployment files
const deploymentFiles = [
  { file: 'Dockerfile', purpose: 'Docker deployment' },
  { file: 'docker-compose.yml', purpose: 'Docker Compose' },
  { file: 'vercel.json', purpose: 'Vercel deployment' },
  { file: 'netlify.toml', purpose: 'Netlify deployment' },
  { file: '.github/workflows', purpose: 'GitHub Actions CI/CD' }
];

deploymentFiles.forEach(({ file, purpose }) => {
  const filePath = path.join(process.cwd(), file);
  if (fs.existsSync(filePath)) {
    console.log(`‚úÖ ${file} exists (${purpose})`);
  }
});

recommendations.push('üí° Consider adding deployment configuration for your chosen platform');

// 9. Data Validation & Testing
console.log('\n9. üß™ Data Validation & Testing');
console.log('-'.repeat(30));

// Check for testing setup
const testFiles = ['jest.config.js', 'playwright.config.ts', '__tests__', 'tests'];
let hasTests = false;
testFiles.forEach(file => {
  if (fs.existsSync(path.join(process.cwd(), file))) {
    console.log(`‚úÖ Testing setup found: ${file}`);
    hasTests = true;
  }
});

if (!hasTests) {
  recommendations.push('üí° Consider adding automated testing (Jest, Playwright, etc.)');
}

// Check Zod validation
if (fs.existsSync(appointmentsApiPath)) {
  const content = fs.readFileSync(appointmentsApiPath, 'utf8');
  if (content.includes('zod') && content.includes('.parse(')) {
    console.log('‚úÖ Input validation with Zod implemented');
  }
}

// 10. Summary Report
console.log('\n' + '='.repeat(60));
console.log('üìä PRODUCTION READINESS SUMMARY');
console.log('='.repeat(60));

console.log(`\nüî¥ Critical Issues (${issues.length}):`);
if (issues.length === 0) {
  console.log('‚úÖ No critical issues found!');
} else {
  issues.forEach(issue => console.log(issue));
}

console.log(`\nüü° Warnings (${warnings.length}):`);
if (warnings.length === 0) {
  console.log('‚úÖ No warnings!');
} else {
  warnings.forEach(warning => console.log(warning));
}

console.log(`\nüí° Recommendations (${recommendations.length}):`);
recommendations.forEach(rec => console.log(rec));

// Overall Score
const totalChecks = 50; // Approximate number of checks
const criticalIssues = issues.length;
const warningIssues = warnings.length;

const score = Math.max(0, totalChecks - (criticalIssues * 3) - (warningIssues * 1));
const percentage = Math.round((score / totalChecks) * 100);

console.log(`\nüéØ Production Readiness Score: ${percentage}/100`);

if (percentage >= 90) {
  console.log('üü¢ EXCELLENT - Ready for production deployment!');
} else if (percentage >= 75) {
  console.log('üü° GOOD - Address critical issues before production');
} else if (percentage >= 60) {
  console.log('üü† FAIR - Significant improvements needed');
} else {
  console.log('üî¥ POOR - Not ready for production');
}

console.log('\nüìã Next Steps:');
console.log('1. Fix all critical issues (üî¥)');
console.log('2. Address warnings (üü°)');
console.log('3. Implement recommendations (üí°)');
console.log('4. Run production build test');
console.log('5. Deploy to staging environment first');
console.log('6. Perform security audit');
console.log('7. Set up monitoring and logging');

console.log('\n‚ú® Assessment completed at:', new Date().toLocaleString());